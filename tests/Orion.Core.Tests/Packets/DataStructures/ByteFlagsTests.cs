using System;
using System.Collections.Generic;
using System.Text;
using Xunit;

namespace Orion.Core.Packets.DataStructures
{
    public class ByteFlagsTests
    {
        [Theory]
        [InlineData(0)]
        [InlineData(1)]
        [InlineData(2)]
        [InlineData(3)]
        [InlineData(4)]
        [InlineData(5)]
        [InlineData(6)]
        [InlineData(7)]
        public void Item_Set_Get(int index)
        {
            var flags = new ByteFlags();

            flags[index] = true;

            Assert.True(flags[index]);

            flags[index] = false;

            Assert.False(flags[index]);
        }
    }
}
